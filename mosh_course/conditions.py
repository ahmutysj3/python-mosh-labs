# Practicing if/else conditionals

its_hot = False
its_cold = True

if its_hot:
    print("It's a hot day")
    print("Drink plenty of water!")
elif its_cold:
    print("It's a cold day")
    print("Remember to dress warm")
else:
    print("Not too hot and not too cold")
    print("It is a perfect day!")
print("Have a nice day!")